// =========================================================
//  i18n Engine
// =========================================================
const I18N = {
  currentLang: localStorage.getItem('ditib_lang') || 'tr',

  t(key) {
    return (translations[this.currentLang] || translations.tr)[key] || key;
  },

  apply(lang) {
    this.currentLang = lang;
    localStorage.setItem('ditib_lang', lang);
    document.documentElement.lang = lang;

    // text content — safe: preserves SVG/IMG child elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const val = this.t(el.dataset.i18n);
      if (!val) return;
      // If the element contains SVG or IMG children, only update text nodes
      // so the visual content is not destroyed by textContent replacement.
      if (el.querySelector('svg, img')) {
        const textNodes = [];
        for (let n = el.firstChild; n; n = n.nextSibling) {
          if (n.nodeType === Node.TEXT_NODE) textNodes.push(n);
        }
        if (textNodes.length) {
          textNodes[0].nodeValue = val;
          for (let i = 1; i < textNodes.length; i++) textNodes[i].remove();
        } else {
          el.prepend(document.createTextNode(val));
        }
      } else {
        el.textContent = val;
      }
    });

    // inner HTML (for strings containing tags)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const val = this.t(el.dataset.i18nHtml);
      if (val) el.innerHTML = val;
    });

    // placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const val = this.t(el.dataset.i18nPlaceholder);
      if (val) el.placeholder = val;
    });

    // aria-label attribute
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const val = this.t(el.dataset.i18nAria);
      if (val) el.setAttribute('aria-label', val);
    });

    // switcher active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Gebetszeiten neu rendern bei Sprachwechsel
    window.PrayerTimes?.rerender?.();
  },

  init() {
    this.apply(this.currentLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => this.apply(btn.dataset.lang));
    });
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
